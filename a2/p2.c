#include <stdio.h>

int main() {
    char c;
    scanf("%c", &c); // gets char input and saves in c //
    printf("character=%c\n", c); // prints as char //
    printf("decimal=%d\n",c); // prints as decimal // 
    printf("octal=%o\n",c); // prints as octal // 
    printf("hexadecimal=%x\n", c); // prints as hexadecimal //

    return 0;
}